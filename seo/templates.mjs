// Six intent templates. Each one renders a structurally different page
// — different section order, different paragraph framing, different
// FAQ — so the generated pages are not near-duplicates of each other.

import { CATEGORIES, YEAR } from './data.mjs';

const fmtRub = (n) => (n ? `от ${n.toLocaleString('ru-RU')} ₽` : 'по сумме брони');
const fmtRubBare = (n) => `${n.toLocaleString('ru-RU')} ₽`;

function tiers(s) {
  if (!s.tiers || !s.tiers.length) return '';
  return s.tiers.map((t) => `<li>${t}</li>`).join('');
}

function relatedLinks(s, intents, currentKey) {
  const items = intents
    .filter((i) => i.key !== currentKey)
    .slice(0, 4)
    .map((i) => `<a href="../${i.slug(s.slug)}/" class="rel-card">${i.title} ${s.name}</a>`)
    .join('');
  return `<div class="rel-grid">${items}</div>`;
}

function faqBlock(items) {
  return items
    .map(
      (q) => `
      <div class="faq-item">
        <h3>${q.q}</h3>
        <p>${q.a}</p>
      </div>`,
    )
    .join('');
}

