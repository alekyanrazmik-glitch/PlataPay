import { SERVICES } from './data.mjs';

const RATE_RUB_PER_USD = 80;
const SERVICE_FEE_RUB = 1000;

const ALIASES = {
  chatgpt: ['ChatGPT Plus', 'ChatGPT Team'],
  claude: ['Claude Pro'],
  gemini: ['Gemini Advanced'],
  'adobe-cc': ['Adobe', 'Adobe CC', 'Adobe Creative Cloud'],
  'adobe-photoshop': ['Photoshop'],
  'youtube-premium': ['YouTube', 'YouTube Premium'],
  spotify: ['Spotify', 'Spotify Premium'],
  canva: ['Canva', 'Canva Pro'],
  capcut: ['CapCut', 'CapCut Pro'],
  zoom: ['Zoom', 'Zoom Pro'],
  discord: ['Discord', 'Discord Nitro'],
  'proton-mail': ['Proton', 'Proton Mail'],
  'google-drive': ['Google Drive'],
  'ps-plus-turkey': ['PS Plus', 'PS Plus (Турция)', 'PlayStation'],
};

function norm(value) {
  return String(value || '')
    .toLowerCase()
    .replace(/premium|pro|plus|creative cloud|cc|тариф|подписка/g, '')
    .replace(/[^a-zа-я0-9]+/gi, '');
}

function rub(value) {
  return Math.round(value * RATE_RUB_PER_USD + SERVICE_FEE_RUB);
}

function formatRub(value) {
  return new Intl.NumberFormat('ru-RU').format(value) + ' ₽';
}

function cleanPlanName(tier) {
  const text = String(tier || '').trim();
  const noPrice = text.replace(/\s*\([^)]*\$[^)]*\)\s*/g, '').replace(/\s*\$\s*\d+(?:[.,]\d+)?\s*/g, '').trim();
  return noPrice || text || 'Тариф';
}

function bestPlanFromTiers(service) {
  const priced = (service.tiers || [])
    .map((tier) => {
      const match = String(tier).match(/\$\s*(\d+(?:[.,]\d+)?)/);
      if (!match) return null;
      const usd = Number(match[1].replace(',', '.'));
      return { plan: cleanPlanName(tier), rub: rub(usd) };
    })
    .filter(Boolean)
    .sort((a, b) => a.rub - b.rub);

  if (!priced.length) return { plan: 'Тариф', text: 'по запросу' };
  const best = priced[0];
  return { ...best, text: `${best.plan} — ${formatRub(best.rub)}` };
}

const displayByName = new Map();
for (const service of SERVICES) {
  const display = bestPlanFromTiers(service).text;
  const names = [service.name, service.slug, ...(ALIASES[service.slug] || [])];
  for (const name of names) displayByName.set(norm(name), display);
}

function displayForName(name) {
  const key = norm(name);
  if (!key) return null;
  if (displayByName.has(key)) return displayByName.get(key);
  for (const [candidate, display] of displayByName.entries()) {
    if (candidate && (candidate === key || candidate.includes(key) || key.includes(candidate))) return display;
  }
  return null;
}

function patchCatalogServiceArray(html) {
  let out = html.replace(/(\{[^{}\n]*name:")([^"]+)("[^{}\n]*price:")([^"]*)(")/g, (full, before, name, middle, oldPrice, after) => {
    const nextDisplay = displayForName(name);
    return nextDisplay ? `${before}${name}${middle}${nextDisplay}${after}` : full;
  });

  // Catalog cards are generated by JS. Remove the hardcoded "от" label so cards show
  // the plan name + price, for example: "Plus — 2 600 ₽".
  out = out
    .replace(
      `+'<div class="pp-price"><span class="pp-from">от</span> <b>'+s.price+'</b></div>'`,
      `+'<div class="pp-price"><b>'+s.price+'</b></div>'`,
    )
    .replace(
      `+(s.price?'<div class="pp-price"><span class="pp-from">от</span> <b>'+s.price+'</b></div>':'')`,
      `+(s.price?'<div class="pp-price"><b>'+s.price+'</b></div>':'')`,
    )
    .replace(/<span class="pp-from">от<\/span>\s*/g, '')
    .replace(/<span>от<\/span>\s*/g, '');
  return out;
}

function patchHomePopularCards(html) {
  return html
    .replace(/(<div class="pp-pc">[\s\S]*?<div class="pp-pc-name">)([^<]+)(<\/div>[\s\S]*?<div class="pp-pc-price">)<span>от<\/span> <b>([^<]*)(<\/b><\/div>)/g, (full, before, name, middle, oldPrice, after) => {
      const nextDisplay = displayForName(name);
      return nextDisplay ? `${before}${name}${middle}<b>${nextDisplay}${after}` : full;
    })
    .replace(/<div class="pp-pc-price"><span>от<\/span>\s*/g, '<div class="pp-pc-price">');
}

export function patchStaticPrices(html) {
  return patchHomePopularCards(patchCatalogServiceArray(html));
}
